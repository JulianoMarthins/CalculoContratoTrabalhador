package entities;

public class Department {

	// Atributps
	private String name;

	// Construtores
	public Department() {

	}

	public Department(String name) {
		this.name = name;
	}

	// Getters & Setters
	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

}
